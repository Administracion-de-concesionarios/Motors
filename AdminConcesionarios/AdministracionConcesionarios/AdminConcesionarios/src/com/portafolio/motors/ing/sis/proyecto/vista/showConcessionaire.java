    /**
     * ESTE METODO SIMPLEMENTE LO UTILIZAREMOS PARA CARGAR LOS DATOS Y
     * MOSTRARLOS EN LA TABLA RESPECTIVAMENTE
     *
     */
    private void mostrarConcesionarios() {

        this.model.addColumn("ID");
        this.model.addColumn("Nombre");
        this.model.addColumn("Direccion");
        this.model.addColumn("Telefono");
        tbConcesionarios.setModel(model);

        Listas<concesionarioBuilder> datos = mensajero.buscarConcesionarios();
        if (datos != null) {
            Object[] filas = new Object[4];
            try {
                for (int i = 0; i < datos.size(); i++) {
                    filas[0] = (datos.get(i).getId());
                    filas[1] = (datos.get(i).getNombre());
                    filas[2] = (datos.get(i).getDireccion());
                    filas[3] = (datos.get(i).getTelefono());
                    model.addRow(filas);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Â¡No se pudieron cargar los datos", "Motors", JOptionPane.ERROR_MESSAGE);
            }

        }

    }

    /**
     * ESTE METODO LO UTILIZAREMOS PARA PODER ELIMINAR LOS CONCESIONARIOS QUE SE
     * DESEEN POR MEDIO DE UNA BUSQUEDA POR ID, PRIMERO SE CARGARAN LOS DATOS
     * DESDE EL MODELO Y LUEGO LO ELIMINAREMOS DESDE EL CONTROLADOR CON LA LISTA
     */
    private void eliminarConcesionario() {

        try {

            int dato = Integer.parseInt(txtSearch.getText());
            Listas<concesionarioBuilder> borrar = mensajero.eliminarConcesionarios(dato);
            if (borrar != null && !txtSearch.getText().equals("")) {
                for (int i = 0; i < borrar.size(); i--) {
                    borrar.remove(i).getId();
                    borrar.remove(i).getNombre();
                    borrar.remove(i).getDireccion();
                    borrar.remove(i).getTelefono();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error al cargar los datos", "Motors", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void filtro() {
        txtSearch.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtSearch.getText(), 0));
            }
        });
        trs = new TableRowSorter(model);
        tbConcesionarios.setRowSorter(trs);
    }

